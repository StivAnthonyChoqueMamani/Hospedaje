    public function update_role(StoreUpdateRoleRequest $request, $id)
    {
        $validate = $request->validated();
        if($id>2){
            User::findOrFail($id)->update($validate);
            $user = User::findOrFail($id)->load('role');
            return response(['user' => $user]);
        }else{
            return response(['error'=> 'No se puede modificar a los 2 primeros usuarios']);
        }
    }

